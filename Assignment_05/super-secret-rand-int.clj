(defn val-at [pos] (- (int (nth seed pos)) 48))

(defn create-numbers [size]
  (loop [n 0 v []]
    (if (= n size)
      (into [] (flatten v))
      (recur (inc n) (conj v (replicate (val-at n) n))))))

(def number-size 10000)

(def numbers (create-numbers number-size))

(def numbers-length (count numbers))

(defn super-secret-rand-int [n]
  (if (= n number-size)
    (nth numbers (rand-int numbers-length))
    (rand-int n)))

